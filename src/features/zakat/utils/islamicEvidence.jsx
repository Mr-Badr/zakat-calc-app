export function getIslamicEvidence(zakatType, madhab) {
  const evidence = {};

  switch (zakatType) {
    case 'money':
      evidence.quranVerse = {
        key: 'quran_3',
        text: "وَالَّذِينَ يَكْنِزُونَ الذَّهَبَ وَالْفِضَّةَ وَلَا يُنفِقُونَهَا فِي سَبِيلِ اللَّهِ فَبَشِّرْهُم بِعَذَابٍ أَلِيمٍ",
        reference: "سورة التوبة - الآية 34"
      };
      evidence.hadith = {
        text: "ما من صاحب ذهب ولا فضة لا يؤدي منها حقها إلا إذا كان يوم القيامة صفحت له صفائح من نار",
        reference: "صحيح مسلم - كتاب الزكاة"
      };
      evidence.consensus = "أجمع العلماء على وجوب الزكاة في الذهب والفضة والنقود إذا بلغت النصاب وحال عليها الحول.";

      if (madhab === 'hanafi') {
        evidence.madhabOpinion = "المذهب الحنفي يعتبر نصاب الفضة (595 جرام) في زكاة النقود لكونه أرفق بالفقراء وأكثر إخراجاً للزكاة، وهو قول أبي حنيفة رحمه الله.";
      } else {
        evidence.madhabOpinion = "جمهور العلماء من المالكية والشافعية والحنابلة يعتبرون نصاب الذهب (85 جرام) في زكاة النقود، وهو الأصل في تقدير النصاب.";
      }
      break;

    case 'gold-silver':
      evidence.quranVerse = {
        key: 'quran_3',
        text: "وَالَّذِينَ يَكْنِزُونَ الذَّهَبَ وَالْفِضَّةَ وَلَا يُنفِقُونَهَا فِي سَبِيلِ اللَّهِ فَبَشِّرْهُم بِعَذَابٍ أَلِيمٍ",
        reference: "سورة التوبة - الآية 34"
      };
      evidence.hadith = {
        text: "في الرقة ربع العشر، وليس فيما دون خمس أواق صدقة، وفي الذهب في كل عشرين ديناراً فصاعداً نصف دينار",
        reference: "سنن أبي داود - كتاب الزكاة"
      };
      evidence.consensus = "أجمع العلماء على وجوب الزكاة في الذهب والفضة بمعدل 2.5% إذا بلغا النصاب وحال عليهما الحول.";
      evidence.madhabOpinion = "جميع المذاهب الأربعة متفقة على أن نصاب الذهب 20 مثقالاً (85 جرام) ونصاب الفضة 200 درهم (595 جرام).";
      break;

    case 'business':
      evidence.quranVerse = {
        key: 'quran_4',
        text: "يَا أَيُّهَا الَّذِينَ آمَنُوا أَنفِقُوا مِن طَيِّبَاتِ مَا كَسَبْتُمْ وَمِمَّا أَخْرَجْنَا لَكُم مِّنَ الْأَرْضِ",
        reference: "سورة البقرة - الآية 267"
      };
      evidence.hadith = {
        text: "ليس على المسلم في عبده ولا فرسه صدقة إلا صدقة الفطر",
        reference: "صحيح البخاري - كتاب الزكاة"
      };
      evidence.consensus = "أجمع العلماء على وجوب الزكاة في عروض التجارة إذا بلغت قيمتها النصاب وحال عليها الحول.";
      evidence.madhabOpinion = "جميع المذاهب تتفق على أن عروض التجارة تُقوَّم بسعر البيع في نهاية الحول وتُخرج زكاتها بمعدل 2.5%.";
      break;

    case 'agriculture':
      evidence.quranVerse = {
        key: 'quran_5',
        text: "وَآتُوا حَقَّهُ يَوْمَ حَصَادِهِ",
        reference: "سورة الأنعام - الآية 141"
      };
      evidence.hadith = {
        text: "فيما سقت السماء والعيون أو كان عثرياً العشر، وفيما سُقي بالنضح نصف العشر",
        reference: "صحيح البخاري - كتاب الزكاة"
      };
      evidence.consensus = "أجمع العلماء على وجوب الزكاة في الزروع والثمار المكيلة المدخرة إذا بلغت خمسة أوسق.";
      evidence.madhabOpinion = "جميع المذاهب تتفق على أن النصاب خمسة أوسق (653 كيلوجرام تقريباً) وأن المعدل 10% للسقي الطبيعي و5% للسقي الصناعي.";
      break;

    case 'livestock':
      evidence.quranVerse = {
        key: 'quran_6',
        text: "وَمِنَ الْأَنْعَامِ حَمُولَةً وَفَرْشًا كُلُوا مِمَّا رَزَقَكُمُ اللَّهُ",
        reference: "سورة الأنعام - الآية 142"
      };
      evidence.hadith = {
        text: "في كل خمس من الإبل شاة، وفي كل أربعين من الغنم شاة، وفي كل ثلاثين من البقر تبيع",
        reference: "سنن أبي داود - كتاب الزكاة"
      };
      evidence.consensus = "أجمع العلماء على وجوب الزكاة في الأنعام السائمة (الإبل والبقر والغنم) إذا بلغت النصاب وحال عليها الحول.";
      evidence.madhabOpinion = "جميع المذاهب تتفق على أنصبة الأنعام ومقادير الزكاة فيها كما وردت في السنة النبوية.";
      break;

    case 'income':
      evidence.quranVerse = {
        key: 'quran_4',
        text: "يَا أَيُّهَا الَّذِينَ آمَنُوا أَنفِقُوا مِن طَيِّبَاتِ مَا كَسَبْتُمْ",
        reference: "سورة البقرة - الآية 267"
      };
      evidence.hadith = {
        text: "ليس فيما دون خمس أواق من الورق صدقة",
        reference: "صحيح البخاري - كتاب الزكاة"
      };
      evidence.consensus = "اختلف العلماء المعاصرون في زكاة الدخل بين من يوجبها شهرياً ومن يوجبها بعد الحول.";

      if (madhab === 'hanafi') {
        evidence.madhabOpinion = "بعض علماء الحنفية المعاصرين يرون جواز إخراج زكاة الدخل شهرياً تيسيراً على المكلف.";
      } else {
        evidence.madhabOpinion = "الأصل في المذاهب الثلاثة أن الزكاة تُخرج بعد مرور الحول على المال المدخر من الدخل.";
      }
      break;

    case 'rental':
      evidence.quranVerse = {
        key: 'quran_7',
        text: "وَآتُوا الزَّكَاةَ وَأَقْرِضُوا اللَّهَ قَرْضًا حَسَنًا",
        reference: "سورة المزمل - الآية 20"
      };
      evidence.hadith = {
        text: "ليس على المسلم في عبده ولا فرسه صدقة",
        reference: "صحيح البخاري - كتاب الزكاة"
      };
      evidence.consensus = "أجمع العلماء على أن الزكاة في إيرادات العقارات تكون على الإيراد وليس على قيمة العقار نفسه.";
      evidence.madhabOpinion = "جميع المذاهب تتفق على أن زكاة إيرادات العقارات تُحسب على صافي الإيراد بعد خصم المصروفات الضرورية.";
      break;

    case 'investments':
      evidence.quranVerse = {
        key: 'quran_4',
        text: "يَا أَيُّهَا الَّذِينَ آمَنُوا أَنفِقُوا مِن طَيِّبَاتِ مَا كَسَبْتُمْ",
        reference: "سورة البقرة - الآية 267"
      };
      evidence.hadith = {
        text: "إن الله طيب لا يقبل إلا طيباً",
        reference: "صحيح مسلم - كتاب الزكاة"
      };
      evidence.consensus = "أجمع العلماء المعاصرون على وجوب الزكاة في الاستثمارات المباحة، مع اختلاف في طريقة الحساب.";
      evidence.madhabOpinion = "العلماء المعاصرون يفرقون بين الاستثمار طويل المدى (زكاة على الأرباح) والتجارة قصيرة المدى (زكاة على القيمة الكاملة).";
      break;

    default:
      evidence.quranVerse = {
        key: 'quran_1',
        text: "وَأَقِيمُوا الصَّلَاةَ وَآتُوا الزَّكَاةَ وَارْكَعُوا مَعَ الرَّاكِعِينَ",
        reference: "سورة البقرة - الآية 43"
      };
      evidence.hadith = {
        text: "بُني الإسلام على خمس: شهادة أن لا إله إلا الله وأن محمداً رسول الله، وإقام الصلاة، وإيتاء الزكاة، والحج، وصوم رمضان",
        reference: "صحيح البخاري ومسلم"
      };
      evidence.consensus = "أجمع العلماء على أن الزكاة ركن من أركان الإسلام وفريضة على كل مسلم بالغ عاقل.";
  }

  return evidence;
}
